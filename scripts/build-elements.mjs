/// @ts-check

/**
 * Generates one self-registering entry module per component under
 * `src/elements/`, plus the per-component subpath `exports` map in
 * `package.json`. Each generated module imports its component and registers
 * every exported custom element, so a no-bundler consumer can
 * `import "@combat-ui/core/<name>"` (or the file URL) and have the element
 * defined with zero ceremony.
 *
 * Run via `npm run elements`. Idempotent — only writes when content changes.
 */

import { existsSync } from "node:fs";
import { mkdir, readdir, readFile, rm, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = resolve(dirname(fileURLToPath(import.meta.url)));
const ROOT = resolve(HERE, "..");
const COMPONENTS_DIR = resolve(ROOT, "src/components");
const ELEMENTS_DIR = resolve(ROOT, "src/elements");
const PKG_PATH = resolve(ROOT, "package.json");

const HEADER = "// Generated by scripts/build-elements.mjs — do not edit by hand.\n";

/** @param {string} name */
function elementSource(name) {
  return `${HEADER}// Self-registering entry for the "${name}" component.
import { defineElement, type CombatElementConstructor } from "../internal/base-element";
import * as mod from "../components/${name}/index";

const exported = mod as unknown as Record<string, unknown>;
for (const value of Object.values(exported)) {
  if (
    typeof value === "function" &&
    typeof (value as { tagName?: unknown }).tagName === "string"
  ) {
    defineElement(value as CombatElementConstructor);
  }
}

export * from "../components/${name}/index";
`;
}

async function collectComponentNames() {
  const dirents = await readdir(COMPONENTS_DIR, { withFileTypes: true });
  const names = [];
  for (const dirent of dirents) {
    if (
      dirent.isDirectory() &&
      existsSync(resolve(COMPONENTS_DIR, dirent.name, "index.ts"))
    ) {
      names.push(dirent.name);
    }
  }
  return names.sort();
}

/** @param {string[]} names */
async function writeElementFiles(names) {
  // Regenerate from scratch so a removed component leaves no straggler entry.
  await rm(ELEMENTS_DIR, { recursive: true, force: true });
  await mkdir(ELEMENTS_DIR, { recursive: true });
  for (const name of names) {
    await writeFile(
      resolve(ELEMENTS_DIR, `${name}.ts`),
      elementSource(name),
      "utf-8",
    );
  }
}

/** @param {string[]} names */
async function updateExportsMap(names) {
  const raw = await readFile(PKG_PATH, "utf-8");
  const pkg = JSON.parse(raw);
  const current = pkg.exports ?? {};

  /** @type {Record<string, unknown>} */
  const next = {
    ".": current["."] ?? {
      types: "./dist/types/index.d.ts",
      import: "./dist/combat-ui.js",
    },
  };
  if (current["./styles"]) next["./styles"] = current["./styles"];

  for (const name of names) {
    next[`./${name}`] = {
      types: `./dist/types/elements/${name}.d.ts`,
      import: `./dist/${name}.js`,
    };
  }

  pkg.exports = next;
  const updated = `${JSON.stringify(pkg, null, 2)}\n`;
  if (updated !== raw) {
    await writeFile(PKG_PATH, updated, "utf-8");
  }
}

async function main() {
  const names = await collectComponentNames();
  await writeElementFiles(names);
  await updateExportsMap(names);
  console.log(
    `Wrote ${names.length} self-registering element entries and subpath exports.`,
  );
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
